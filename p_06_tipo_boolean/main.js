// operadores de comparação
// == comparação de valor
// !=
console.log(5 == "5");//true

// === comparação de tipo e valor
// !===
console.log(5 === "5");//false

// >
console.log(5 > 10);

// <
console.log(5 < 10);

// >=
console.log(5 >= 10);

// <=
console.log(5 <= 10);

