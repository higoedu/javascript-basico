// + soma
console.log(11 + 2);

// - subtração
console.log(11 - 2);

// * multiplicação
console.log(11 * 2);

// / divisão
console.log(11 / 2);

// % resta da divisão
console.log(11 % 2);

// (1 + 1) * 3, 1º soma 1 + 1, depois multiplica-se por 3
console.log((1 / 1) * 3);

// parseInt(11 / 2) - função embutida no javascript, retorna o valor inteiro
console.log(parseInt(11 / 2));

let numeroRetornado = parseInt("1")//transforma string para inteito

console.log(numeroRetornado);

console.log(typeof numeroRetornado);//retorna number

let numeroRetornado2 = parseFloat("1.1")//transforma string para ponto flutuante

console.log(numeroRetornado2);

console.log(typeof numeroRetornado2);//retorna number

